import type { Router } from '../http/Router.js';

/**
 * Базовый класс модуля приложения.
 *
 * Модуль — это логическая единица функциональности (например, UsersModule).
 * Он регистрирует маршруты через метод `register()` и может выполнять
 * инициализацию при старте (`onStart`) и очистку при остановке (`onStop`).
 *
 * Это паттерн «Module» из чистой архитектуры — помогает группировать
 * связанный код и управлять его жизненным циклом.
 */
export abstract class AppModule {
  /** Регистрирует маршруты модуля в роутере приложения. */
  abstract register(router: Router): void;

  /** Вызывается при старте приложения — подключения к БД, инициализация кэша. */
  onStart(): Promise<void> | void {}

  /** Вызывается при остановке приложения — закрытие соединений, сохранение состояния. */
  onStop(): Promise<void> | void {}
}
