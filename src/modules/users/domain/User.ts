/**
 * Доменная сущность пользователя.
 * Содержит идентификатор, email, хэш пароля и дату создания аккаунта.
 */
export interface User {
  /** Уникальный идентификатор пользователя (UUID) */
  id: string;
  /** Электронная почта пользователя */
  email: string;
  /** Хэш пароля, полученный через алгоритм scrypt */
  passwordHash: string;
  /** Дата и время создания пользователя */
  createdAt: Date;
}
