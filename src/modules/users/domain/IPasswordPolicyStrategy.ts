/**
 * Стратегия политики паролей.
 * Реализует паттерн «Стратегия» для гибкой замены
 * требований к сложности пароля без изменения бизнес-логики.
 */
export interface IPasswordPolicyStrategy {
  /**
   * Проверяет пароль на соответствие политике безопасности.
   * @param password - пароль в открытом виде для проверки
   * @returns массив строк с описанием нарушений; пустой массив означает, что пароль допустим
   */
  validate(password: string): string[];
}
