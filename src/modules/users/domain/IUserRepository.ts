import type { User } from './User.js';

/**
 * Порт репозитория пользователей.
 * Определяет контракт для работы с хранилищем пользователей
 * в соответствии с принципом инверсии зависимостей.
 */
export interface IUserRepository {
  /**
   * Находит пользователя по электронной почте.
   * @param email - адрес электронной почты для поиска
   * @returns найденный пользователь или null, если не существует
   */
  findByEmail(email: string): Promise<User | null>;

  /**
   * Сохраняет нового пользователя в хранилище.
   * @param user - доменная сущность пользователя для сохранения
   */
  save(user: User): Promise<void>;
}
